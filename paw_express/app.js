let createError = require('http-errors');
let express = require('express');
let path = require('path');
let cookieParser = require('cookie-parser');
let logger = require('morgan');

let indexRouter = require('./routes/index');
let usersRouter = require('./routes/users');
let jsonPrintRouter = require('./routes/json_print.js');
let htmlInRouter = require('./routes/htmlIn.js');
let htmlOutRouter = require('./routes/htmlOut.js');
let paramsRouter = require('./routes/params.js');
let mimeRouter = require('./routes/mime.js');

let app = express();

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'jade');

app.use(logger('dev'));
app.use(express.json());
app.use(express.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));

app.use('/', indexRouter);
app.use('/users', usersRouter);
app.use('/json', jsonPrintRouter);
app.use('/htmlIn', htmlInRouter);
app.use('/htmlOut', htmlOutRouter);
app.use('/page',paramsRouter);
app.use('mime', mimeRouter);

// catch 404 and forward to error handler
app.use(function(req, res, next) {
  next(createError(404));
});

// error handler
app.use(function(err, req, res, next) {
  // set locals, only providing error in development
  res.locals.message = err.message;
  res.locals.error = req.app.get('env') === 'development' ? err : {};

  // render the error page
  res.status(err.status || 500);
  res.render('error');
});

module.exports = app;
